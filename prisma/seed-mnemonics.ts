async function main() {
  console.log("Seeding mnemonics for 解表药...")
  let count = 0
  for (const [name, data] of Object.entries(mnemonics)) {
    await prisma.herbCard.updateMany({
      where: { name },
      data: { mnemonic: data.mnemonic },
    })
    count++
  }

  console.log(`Updated ${count} herbs with mnemonics.`)
}

main().catch(console.error).finally(() => prisma.$disconnect())
